function normalizeTeamName(teamName) {
  return teamName
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .trim()
}

const NORMALIZED_TEAM_CODES = Object.fromEntries(
  Object.entries(TEAM_CODES).map(([teamName, code]) => [
    normalizeTeamName(teamName),
    code,
  ]),
)

export function getTeamCode(teamName) {
  if (!teamName) {
    return ''
  }

  return NORMALIZED_TEAM_CODES[normalizeTeamName(teamName)] || teamName
}
